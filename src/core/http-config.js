const parseUserAgentList = (value) => {
    if (Array.isArray(value)) {
        return value
            .map((entry) => (entry === undefined || entry === null ? '' : String(entry).trim()))
            .filter((entry) => entry.length > 0);
    }

    if (typeof value !== 'string') {
        return [];
    }

    return value
        .split(/\r?\n|\|/)
        .map((entry) => entry.trim())
        .filter((entry) => entry.length > 0);
};

